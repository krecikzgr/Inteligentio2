import Foundation
import UIKit

extension UIColor {
    class func mainBackground() -> UIColor {
        return UIColor(red: 28.0/255.0, green: 35.0/255.0, blue: 43.0/255.0, alpha: 1.0)
    }

    class func mainBlue() -> UIColor {
        return UIColor(red: 0.0/255.0, green:121.0/255.0, blue: 255.0/255.0, alpha: 1.0)
    }
}
